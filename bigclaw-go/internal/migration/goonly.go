func defaultStrategy() Strategy {
	return Strategy{
		BranchPrefix:    "symphony/",
		PRTitleTemplate: "BIG-VNEXT-GO-xxx: <slice title>",
		ExecutionPolicy: []string{
			"Keep slices branch-local and small enough to merge independently into `main`.",
			"Do not mix runtime migration, test migration, and toolchain retirement in one PR unless a later slice depends on the previous merge.",
			"Use the generated slice identifiers as the stable Symphony branch and tracker references.",
		},
		ValidationPolicy: []string{
			"Every slice must run targeted `go test` commands for the touched Go packages.",
			"Tracker closeout must record exact commands and pass/fail results, not generic statements.",
			"Cutover-facing slices must also rerun `go test ./...` from `bigclaw-go` before merge.",
		},
		CutoverPolicy: []string{
			"Do not delete Python or shell surfaces until an equivalent Go-owned runtime or validation path exists.",
			"Prefer staged retirement: generate parity evidence, merge the Go replacement, then freeze and remove the legacy path.",
			"Treat `main` as the only evolving branch; all migration branches rebase on current `main` before merge.",
		},
		FirstBatchExecution: []string{
			"Start `BIG-VNEXT-GO-102`, `BIG-VNEXT-GO-103`, and `BIG-VNEXT-GO-104` in parallel after this planning slice lands.",
			"Keep `BIG-VNEXT-GO-105` blocked until the root test and ops wrapper replacements have converged enough to avoid duplicating harness work.",
			"Use `BIG-VNEXT-GO-101` as the source of truth for remaining non-Go inventory and next-batch prioritization.",
		},
	}
}
