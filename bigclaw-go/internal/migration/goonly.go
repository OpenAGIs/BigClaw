type FileRecord struct {
	Path     string `json:"path"`
	Category string `json:"category"`
	Language string `json:"language"`
}

type Summary struct {
	NonGoFiles             int `json:"non_go_files"`
	LegacyRuntimeFiles     int `json:"legacy_runtime_files"`
	LegacyTestFiles        int `json:"legacy_test_files"`
	PythonScriptFiles      int `json:"python_script_files"`
	ShellScriptFiles       int `json:"shell_script_files"`
	PythonToolchainFiles   int `json:"python_toolchain_files"`
	ValidationHarnessFiles int `json:"validation_harness_files"`
	FirstBatchSliceCount   int `json:"first_batch_slice_count"`
	ParallelSliceCount     int `json:"parallel_slice_count"`
}

type Slice struct {
	Identifier   string   `json:"identifier"`
	Title        string   `json:"title"`
	Phase        string   `json:"phase"`
	Priority     string   `json:"priority"`
	Status       string   `json:"status"`
	FirstBatch   bool     `json:"first_batch"`
	Scope        string   `json:"scope"`
	TargetPaths  []string `json:"target_paths"`
	Dependencies []string `json:"dependencies,omitempty"`
	Validation   []string `json:"validation"`
}

type Strategy struct {
	BranchPrefix        string   `json:"branch_prefix"`
	PRTitleTemplate     string   `json:"pr_title_template"`
	ExecutionPolicy     []string `json:"execution_policy"`
	ValidationPolicy    []string `json:"validation_policy"`
	CutoverPolicy       []string `json:"cutover_policy"`
	FirstBatchExecution []string `json:"first_batch_execution"`
}

type Report struct {
	Summary   Summary      `json:"summary"`
	Inventory []FileRecord `json:"inventory"`
	Slices    []Slice      `json:"slices"`
	Strategy  Strategy     `json:"strategy"`
}

func BuildReport(repoRoot string) (Report, error) {
	inventory, err := scanInventory(repoRoot)
	if err != nil {
		return Report{}, err
	}
	slices := defaultSlices()
	report := Report{
		Inventory: inventory,
		Slices:    slices,
		Strategy:  defaultStrategy(),
	}
	report.Summary = buildSummary(inventory, slices)
	return report, nil
}

func MarshalReport(report Report) ([]byte, error) {
	return json.MarshalIndent(report, "", "  ")
}

func RenderMarkdown(report Report) string {
	var b strings.Builder
	b.WriteString("# BigClaw Go-Only Migration Plan\n\n")
	b.WriteString("This plan is generated by `bigclawctl go-migration plan` and is the repo-native control surface for retiring the remaining Python and non-Go implementation paths.\n\n")
	b.WriteString("## Acceptance\n\n")
	b.WriteString("- Maintain `main` as Go-first while retiring remaining Python and non-Go runtime, script, test, and toolchain surfaces in batches.\n")
	b.WriteString("- Keep the migration inventory explicit so every remaining non-Go path has an owner and a retirement lane.\n")
	b.WriteString(fmt.Sprintf("- Run at least %d parallel slices under Symphony without forcing one large cutover PR.\n\n", report.Summary.ParallelSliceCount))

	b.WriteString("## Inventory Summary\n\n")
	b.WriteString(fmt.Sprintf("- Remaining non-Go files tracked: `%d`\n", report.Summary.NonGoFiles))
	b.WriteString(fmt.Sprintf("- Legacy runtime Python files under `src/bigclaw/`: `%d`\n", report.Summary.LegacyRuntimeFiles))
	b.WriteString(fmt.Sprintf("- Legacy Python test files under `tests/`: `%d`\n", report.Summary.LegacyTestFiles))
	b.WriteString(fmt.Sprintf("- Python script files: `%d`\n", report.Summary.PythonScriptFiles))
	b.WriteString(fmt.Sprintf("- Shell script files: `%d`\n", report.Summary.ShellScriptFiles))
	b.WriteString(fmt.Sprintf("- Python toolchain files: `%d`\n", report.Summary.PythonToolchainFiles))
	b.WriteString(fmt.Sprintf("- Validation-harness Python/shell files under `bigclaw-go/scripts/`: `%d`\n\n", report.Summary.ValidationHarnessFiles))

	b.WriteString("Canonical machine-readable inventory: `docs/reports/go-only-migration-inventory.json`\n\n")

	b.WriteString("## Parallel Slice Plan\n\n")
	for _, slice := range report.Slices {
		firstBatch := "no"
		if slice.FirstBatch {
			firstBatch = "yes"
		}
		b.WriteString(fmt.Sprintf("### %s %s\n\n", slice.Identifier, slice.Title))
		b.WriteString(fmt.Sprintf("- Phase: `%s`\n", slice.Phase))
		b.WriteString(fmt.Sprintf("- Priority: `%s`\n", slice.Priority))
		b.WriteString(fmt.Sprintf("- First batch: `%s`\n", firstBatch))
		b.WriteString(fmt.Sprintf("- Scope: %s\n", slice.Scope))
		b.WriteString("- Target paths:\n")
		for _, path := range slice.TargetPaths {
			b.WriteString(fmt.Sprintf("  - `%s`\n", path))
		}
		if len(slice.Dependencies) > 0 {
			b.WriteString("- Dependencies:\n")
			for _, dependency := range slice.Dependencies {
				b.WriteString(fmt.Sprintf("  - `%s`\n", dependency))
			}
		}
		b.WriteString("- Validation:\n")
		for _, validation := range slice.Validation {
			b.WriteString(fmt.Sprintf("  - `%s`\n", validation))
		}
		b.WriteString("\n")
	}

	b.WriteString("## Branch And PR Strategy\n\n")
	b.WriteString(fmt.Sprintf("- Branch naming: `%s<slice-id>`\n", report.Strategy.BranchPrefix))
	b.WriteString(fmt.Sprintf("- PR title template: `%s`\n", report.Strategy.PRTitleTemplate))
	for _, line := range report.Strategy.ExecutionPolicy {
		b.WriteString(fmt.Sprintf("- %s\n", line))
	}
	for _, line := range report.Strategy.ValidationPolicy {
		b.WriteString(fmt.Sprintf("- %s\n", line))
	}
	for _, line := range report.Strategy.CutoverPolicy {
		b.WriteString(fmt.Sprintf("- %s\n", line))
	}
	b.WriteString("\n## First Batch Execution\n\n")
	for _, line := range report.Strategy.FirstBatchExecution {
		b.WriteString(fmt.Sprintf("- %s\n", line))
	}
	return b.String()
}

func scanInventory(repoRoot string) ([]FileRecord, error) {
	records := []FileRecord{}
	err := filepath.WalkDir(repoRoot, func(path string, d fs.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		rel, err := filepath.Rel(repoRoot, path)
		if err != nil {
			return err
		}
		rel = filepath.ToSlash(rel)
		if rel == "." {
			return nil
		}
		if d.IsDir() {
			switch rel {
			case ".git", ".symphony":
				return filepath.SkipDir
			}
			return nil
		}
		record, ok := classifyFile(repoRoot, rel)
		if ok {
			records = append(records, record)
		}
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Slice(records, func(i, j int) bool {
		if records[i].Category == records[j].Category {
			return records[i].Path < records[j].Path
		}
		return records[i].Category < records[j].Category
	})
	return records, nil
}

func classifyFile(repoRoot string, relativePath string) (FileRecord, bool) {
	lower := strings.ToLower(relativePath)
	extension := strings.ToLower(filepath.Ext(lower))
	switch {
	case lower == "setup.py" || lower == "pyproject.toml":
		return FileRecord{Path: relativePath, Category: "python-toolchain", Language: "python"}, true
	case strings.HasPrefix(lower, "src/bigclaw/") && extension == ".py":
		return FileRecord{Path: relativePath, Category: "legacy-runtime", Language: "python"}, true
	case strings.HasPrefix(lower, "tests/") && extension == ".py":
		return FileRecord{Path: relativePath, Category: "legacy-tests", Language: "python"}, true
	case strings.HasPrefix(lower, "scripts/") && extension == ".py":
		return FileRecord{Path: relativePath, Category: "repo-script", Language: "python"}, true
	case strings.HasPrefix(lower, "scripts/") && (extension == ".sh" || isScriptWithoutExtension(repoRoot, relativePath)):
		return FileRecord{Path: relativePath, Category: "repo-script", Language: "shell"}, true
	case strings.HasPrefix(lower, "bigclaw-go/scripts/") && extension == ".py":
		return FileRecord{Path: relativePath, Category: "validation-harness", Language: "python"}, true
	case strings.HasPrefix(lower, "bigclaw-go/scripts/") && (extension == ".sh" || isScriptWithoutExtension(repoRoot, relativePath)):
		return FileRecord{Path: relativePath, Category: "validation-harness", Language: "shell"}, true
	default:
		return FileRecord{}, false
	}
}

func isScriptWithoutExtension(repoRoot string, relativePath string) bool {
	if filepath.Ext(relativePath) != "" {
		return false
	}
	body, err := os.ReadFile(filepath.Join(repoRoot, relativePath))
	if err != nil {
		return false
	}
	return strings.HasPrefix(string(body), "#!")
}

func buildSummary(inventory []FileRecord, slices []Slice) Summary {
	summary := Summary{NonGoFiles: len(inventory), ParallelSliceCount: len(slices)}
	for _, record := range inventory {
		switch record.Category {
		case "legacy-runtime":
			summary.LegacyRuntimeFiles++
		case "legacy-tests":
			summary.LegacyTestFiles++
		case "repo-script":
			if record.Language == "python" {
				summary.PythonScriptFiles++
			}
			if record.Language == "shell" {
				summary.ShellScriptFiles++
			}
		case "python-toolchain":
			summary.PythonToolchainFiles++
		case "validation-harness":
			summary.ValidationHarnessFiles++
			if record.Language == "python" {
				summary.PythonScriptFiles++
			}
			if record.Language == "shell" {
				summary.ShellScriptFiles++
			}
		}
	}
	for _, slice := range slices {
		if slice.FirstBatch {
			summary.FirstBatchSliceCount++
		}
	}
	return summary
}

